import numpy as np
arr=np.array([1,2,3,4,5])
print("Original Array:",arr)
print("Array +2: ",arr+2)
print("Array *3: ",arr*3)
print("square of the array: ",arr**2)
# aggregate functions
print("sum: ",np.sum(arr))
print("Mean: ",np.mean(arr))
print("Median: ",np.median(arr))
print("Maximum: ",np.max(arr))
